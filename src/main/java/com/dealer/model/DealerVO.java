package com.dealer.model;

import java.io.Serializable;

public class DealerVO implements Serializable{
	private Integer dealerNo;
	private String dealerPassword;
	private String dealerName;
	private String dealerSex;
	private String dealerAddress;
	private String dealerPhone;
	private String dealerMail;
	private Integer dealerState;
	private String dealerAccount;
	
	public Integer getDealerNo() {
		return dealerNo;
	}
	public void setDealerNo(Integer dealerNo) {
		this.dealerNo = dealerNo;
	}
	public String getDealerPassword() {
		return dealerPassword;
	}
	public void setDealerPassword(String dealerPassword) {
		this.dealerPassword = dealerPassword;
	}
	public String getDealerName() {
		return dealerName;
	}
	public void setDealerName(String dealerName) {
		this.dealerName = dealerName;
	}
	public String getDealerSex() {
		return dealerSex;
	}
	public void setDealerSex(String dealerSex) {
		this.dealerSex = dealerSex;
	}
	public String getDealerAddress() {
		return dealerAddress;
	}
	public void setDealerAddress(String dealerAddress) {
		this.dealerAddress = dealerAddress;
	}
	public String getDealerPhone() {
		return dealerPhone;
	}
	public void setDealerPhone(String dealerPhone) {
		this.dealerPhone = dealerPhone;
	}
	public String getDealerMail() {
		return dealerMail;
	}
	public void setDealerMail(String dealerMail) {
		this.dealerMail = dealerMail;
	}
	public Integer getDealerState() {
		return dealerState;
	}
	public void setDealerState(Integer dealerState) {
		this.dealerState = dealerState;
	}
	public String getDealerAccount() {
		return dealerAccount;
	}
	public void setDealerAccount(String dealerAccount) {
		this.dealerAccount = dealerAccount;
	}
	
	
	


	
}
