package com.facility.model;

public class FacilityVO {
	private Integer facilityNo;
	private Integer television;
	private Integer	wifi;
	private Integer kitchen;
	private Integer parking;
	private Integer bathroom;
	private Integer airConditioning;
	private Integer pet;
	private Integer toiletries;
	private Integer roomPhone;        
	private Integer roomBedService;
	
	public Integer getFacilityNo() {
		return facilityNo;
	}
	public void setFacilityNo(Integer facilityNo) {
		this.facilityNo = facilityNo;
	}
	public Integer getTelevision() {
		return television;
	}
	public void setTelevision(Integer television) {
		this.television = television;
	}
	public Integer getWifi() {
		return wifi;
	}
	public void setWifi(Integer wifi) {
		this.wifi = wifi;
	}
	public Integer getKitchen() {
		return kitchen;
	}
	public void setKitchen(Integer kitchen) {
		this.kitchen = kitchen;
	}
	public Integer getParking() {
		return parking;
	}
	public void setParking(Integer parking) {
		this.parking = parking;
	}
	public Integer getBathroom() {
		return bathroom;
	}
	public void setBathroom(Integer bathroom) {
		this.bathroom = bathroom;
	}
	public Integer getAirConditioning() {
		return airConditioning;
	}
	public void setAirConditioning(Integer airConditioning) {
		this.airConditioning = airConditioning;
	}
	public Integer getPet() {
		return pet;
	}
	public void setPet(Integer pet) {
		this.pet = pet;
	}
	public Integer getToiletries() {
		return toiletries;
	}
	public void setToiletries(Integer toiletries) {
		this.toiletries = toiletries;
	}
	public Integer getRoomPhone() {
		return roomPhone;
	}
	public void setRoomPhone(Integer roomPhone) {
		this.roomPhone = roomPhone;
	}
	public Integer getRoomBedService() {
		return roomBedService;
	}
	public void setRoomBedService(Integer roomBedService) {
		this.roomBedService = roomBedService;
	}
	
	
}
